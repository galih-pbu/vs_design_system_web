  String getIconName(String path) {
    String iconName = path.split('/').last;
    List<String> parts = iconName.split('-');
    String result = parts[0];
    for (int i = 1; i < parts.length; i++) {
      String part = parts[i];
      if (part.isNotEmpty && RegExp(r'[a-zA-Z]').hasMatch(part[0])) {
        result += part[0].toUpperCase() + part.substring(1);
      } else {
        result += part;
      }
    }
    return result;
  }

